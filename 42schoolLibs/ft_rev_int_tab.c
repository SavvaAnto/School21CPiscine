void	ft_rev_int_tab(int *tab, int size)
{
	int arr[size];
	int i;

	i = 0;
	while (i < size)
	{
		arr[i] = tab[size - 1 - i];
		i++;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = arr[i];
		i++;
	}
}
